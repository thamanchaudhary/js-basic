/* JavaScript Data Types
    - string
    - number
    - boolean
    - object
    - function
    - undefined
    - null
    - symbol
*/

// String
const stringExample = "Thaman Ram Tharu";
 stExample = typeof stringExample;
 console.log(stExample); // string

// Number
const numberExample = 123;
 numExample = typeof numberExample;
 console.log(numExample); // number

// Boolean
const booleanExample = true;
 boolExample = typeof booleanExample;
 console.log(boolExample); // boolean

// Object
const objectExample = {name: "Thaman", age: 25};
 objExample = typeof objectExample;
 console.log(objExample); // object

// Function
const functionExample = function() {
    return "Hello Thaman";
}
 funcExample = typeof functionExample;
 console.log(funcExample); // function

// Undefined
let undefinedExample;
 undExample = typeof undefinedExample;
 console.log(undExample); // undefined


// Null
const nullExample = null;
 nulExample = typeof nullExample;
 console.log(nulExample); // object

// Symbol
const symbolExample = Symbol();
 symExample = typeof symbolExample;
 console.log(symExample); // symbol

 


